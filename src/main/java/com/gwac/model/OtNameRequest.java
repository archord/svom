public class OtNameRequest {
  
  private float xtemp;
  private float ytemp;
  private String identifyStr;

  /**
   * @return the xtemp
   */
  public float getXtemp() {
    return xtemp;
  }

  /**
   * @param xtemp the xtemp to set
   */
  public void setXtemp(float xtemp) {
    this.xtemp = xtemp;
  }

  /**
   * @return the ytemp
   */
  public float getYtemp() {
    return ytemp;
  }

  /**
   * @param ytemp the ytemp to set
   */
  public void setYtemp(float ytemp) {
    this.ytemp = ytemp;
  }

  /**
   * @return the identifyStr
   */
  public String getIdentifyStr() {
    return identifyStr;
  }

  /**
   * @param identifyStr the identifyStr to set
   */
  public void setIdentifyStr(String identifyStr) {
    this.identifyStr = identifyStr;
  }
}
